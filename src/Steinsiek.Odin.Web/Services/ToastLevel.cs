namespace Steinsiek.Odin.Web.Services;

/// <summary>
/// Represents the severity level of a toast notification.
/// </summary>
public enum ToastLevel
{
    /// <summary>
    /// Informational message.
    /// </summary>
    Info,

    /// <summary>
    /// Success message.
    /// </summary>
    Success,

    /// <summary>
    /// Warning message.
    /// </summary>
    Warning,

    /// <summary>
    /// Error message.
    /// </summary>
    Error
}
